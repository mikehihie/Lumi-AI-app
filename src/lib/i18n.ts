export function t(key: keyof typeof translations.en, lang: 'en' | 'vi', ...args: any[]) {
  let text = translations[lang][key] || translations.en[key] || key;
  args.forEach((arg, i) => {
    text = text.replace(`{${i}}`, arg);
  });
  return text;
}
